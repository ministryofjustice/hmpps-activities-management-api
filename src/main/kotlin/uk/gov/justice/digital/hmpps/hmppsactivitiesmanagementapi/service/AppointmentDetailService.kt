@Service
class AppointmentDetailService(
  private val appointmentRepository: AppointmentRepository,
  private val locationService: LocationService,
  private val prisonerSearchApiClient: PrisonerSearchApiClient,
  private val prisonApiClient: PrisonApiClient,
) {
  fun getAppointmentDetailById(appointmentId: Long) {
    val appointment = appointmentRepository.findOrThrowNotFound(appointmentId)

    val locationMap = locationService.getLocationsForAppointmentsMap(appointment.prisonCode, listOf(appointment.internalLocationId))

    val prisonerMap = prisonerSearchApiClient.findByPrisonerNumbers(appointment.prisonerNumbers()).block()
  }
}
